package day03_comments_escape_sequence;

public class TabAndNextLine {public static void main (String [] args) {
    System.out.println("This os not tabbed");
    System.out.println("\tThis is tabbed 1 times"); // this is tabbed 1 time
    System.out.println("    This os not tabbed");// 4 space is equal 1 tab
    System.out.println("\t\tThis is tabbed 2 times"); // this is tabbed 2 time
    System.out.println("\n1) Go to store\n2) grab milk\n3) pay cashier");
}
}
